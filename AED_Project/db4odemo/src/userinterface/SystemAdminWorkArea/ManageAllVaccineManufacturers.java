    public void populatetable(){
        DefaultTableModel dtm = (DefaultTableModel) tblManufacturers.getModel();
        dtm.setRowCount(0);
            for(VaccineManufacturer vm : ecosystem.getVaccineManufacturerDirectory().getVaccineManufacturerDirectory()){
            Object [] row = new Object[3];
            row[0] = vm;
            row[1] = vm.getVaccineName();
            row[2] = vm.getCurrentStock();
            dtm.addRow(row);
        }

    }
    
     public void updatetable(){
        DefaultTableModel dtm = (DefaultTableModel) tblManufacturers.getModel();
        dtm.setRowCount(0);
        for(VaccineManufacturer vm : ecosystem.getVaccineManufacturerDirectory().getVaccineManufacturerDirectory()){
            Object [] row = new Object[3];
            row[0] = vm;
            row[1] = vm.getVaccineName();
            row[2] = vm.getCurrentStock();
            dtm.addRow(row);
        }
    }

    
