    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
       int selectedRow = tblManufacturers.getSelectedRow();
       VaccineManufacturer vm = (VaccineManufacturer)tblManufacturers.getValueAt(selectedRow, 0);
       UpdateVaccineManufacturer edit = new UpdateVaccineManufacturer(userProcessContainer, ecosystem, vaccineManufacturerDirectory, vm);
       userProcessContainer.add("UpdateVaccineManufacturer", edit);
       CardLayout layout=(CardLayout)userProcessContainer.getLayout();
       layout.next(userProcessContainer);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManufacturers.getSelectedRow();
        VaccineManufacturer vm = (VaccineManufacturer) tblManufacturers.getValueAt(selectedRow, 0);
        vaccineManufacturerDirectory.removeManufacturer(vm);
        populatetable();
        JOptionPane.showMessageDialog(null, "Entry deleted");
    }//GEN-LAST:event_btnDeleteActionPerformed


