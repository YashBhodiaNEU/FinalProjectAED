    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblVaccinator.getModel();
        dtm.setRowCount(0);
        for(Vaccinator sm : ecosystem.getVaccinatorDirectory().getVaccinatorDirectory()){
            if(userAccount.getEmployee().getName().equalsIgnoreCase(sm.getCenterName())){
            Object [] row = new Object[2];
            row[0] = sm;
            row[1] = sm.getVaccinatorEmailID();
            dtm.addRow(row);
        }
      }
 }
    
